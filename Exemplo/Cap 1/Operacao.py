# Operações em Python: Adição, subtração, divisão, divisão inteira, multiplicação, exponenciação e módulo.

a = 5
b = 2
print(a + b) # Adição
print(a - b) # Subtração
print(a / b) # Divisão
print(a // b) # Divisão Inteira
print(a * b) # Multiplicação
print(a ** b) # Exponenciação
print(a % b) # Módulo