# Booleano é um tipo de dado que pode ser verdadeiro ou falso

# Exemplo de variável booleana
verdadeiro = True
falso = False

# Exemplo de operações com booleanos
print(verdadeiro and falso) # False
print(verdadeiro or falso) # True
print(not verdadeiro) # False
print(not falso) # True

# Exemplo de comparação
print(1 == 1) # True
print(1 != 1) # False
print(1 > 1) # False
print(1 < 1) # False
print(1 >= 1) # True
print(1 <= 1) # True