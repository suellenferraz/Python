# Operadores relacionais em Python

# Operador de igualdade
# O operador de igualdade (==) verifica se dois valores são iguais.
# Exemplo:
# Verifica se 5 é igual a 5.
print(5 == 5)  # True
# Verifica se 5 é igual a 10.
print(5 == 10)  # False

# Operador de diferença
# O operador de diferença (!=) verifica se dois valores são diferentes.
# Exemplo:
# Verifica se 5 é diferente de 5.
print(5 != 5)  # False
# Verifica se 5 é diferente de 10.
print(5 != 10)  # True

# Operador de maior que
# O operador de maior que (>) verifica se um valor é maior que outro.
# Exemplo:
# Verifica se 5 é maior que 5.
print(5 > 5)  # False
# Verifica se 5 é maior que 10.
print(5 > 10)  # False
# Verifica se 10 é maior que 5.
print(10 > 5)  # True

# Operador de menor que
# O operador de menor que (<) verifica se um valor é menor que outro.
# Exemplo:
# Verifica se 5 é menor que 5.
print(5 < 5)  # False
# Verifica se 5 é menor que 10.
print(5 < 10)  # True
# Verifica se 10 é menor que 5.
print(10 < 5)  # False

# Operador de maior ou igual a
# O operador de maior ou igual a (>=) verifica se um valor é maior ou igual a outro.
# Exemplo:
# Verifica se 5 é maior ou igual a 5.
print(5 >= 5)  # True
# Verifica se 5 é maior ou igual a 10.
print(5 >= 10)  # False
# Verifica se 10 é maior ou igual a 5.
print(10 >= 5)  # True

# Operador de menor ou igual a
# O operador de menor ou igual a (<=) verifica se um valor é menor ou igual a outro.
# Exemplo:
# Verifica se 5 é menor ou igual a 5.
print(5 <= 5)  # True
# Verifica se 5 é menor ou igual a 10.
print(5 <= 10)  # True
# Verifica se 10 é menor ou igual a 5.
print(10 <= 5)  # False

# Operadores de igualdade e diferença
# Os operadores de igualdade e diferença também podem ser usados com strings.
# Exemplo:
# Verifica se as strings 'Python' e 'Python' são iguais.
print('Python' == 'Python')  # True
# Verifica se as strings 'Python' e 'Java' são iguais.
print('Python' == 'Java')  # False
# Verifica se as strings 'Python' e 'Java' são diferentes.
print('Python' != 'Java')  # True
# Verifica se as strings 'Python' e 'Python' são diferentes.
print('Python' != 'Python')  # False

# Operadores de comparação
# Os operadores de comparação podem ser usados com variáveis.
# Exemplo:
# Atribui o valor 5 à variável x.
x = 5
# Verifica se x é igual a 5.
print(x == 5)  # True
# Verifica se x é diferente de 5.
print(x != 5)  # False
# Verifica se x é maior que 5.
print(x > 5)  # False
# Verifica se x é menor que 5.
print(x < 5)  # False
# Verifica se x é maior ou igual a 5.
print(x >= 5)  # True
# Verifica se x é menor ou igual a 5.
print(x <= 5)  # True
