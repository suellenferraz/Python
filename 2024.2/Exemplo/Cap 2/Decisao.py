# Exemplo de estrutura de decisão com if, elif e else.
# Neste exemplo, o programa solicita um número ao usuário e verifica se ele é positivo, negativo ou zero.

# Solicita um número ao usuário.
numero = float(input('Digite um número: '))
# Verifica se o número é positivo.
if numero > 0:
    print('O número é positivo.')
# Verifica se o número é negativo.
elif numero < 0:
    print('O número é negativo.')
# Se o número não for positivo nem negativo, ele é zero.
else:
    print('O número é zero.')
# Fim do programa.

# O programa solicita um número ao usuário e verifica se ele é positivo, negativo ou zero.
# Se o número for positivo, ele exibe a mensagem "O número é positivo.".
# Se o número for negativo, ele exibe a mensagem "O número é negativo.".
# Se o número for zero, ele exibe a mensagem "O número é zero.".
