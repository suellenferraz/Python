# Conversão de tipos de dados em Python e Utilização do len() para obter o tamanho de uma string.

# String para Inteiro
a = "10"
print(int(a)) # Conversão de String para Inteiro

# String para Float
b = "20.5"
print(float(b)) # Conversão de String para Float

# Inteiro e Float para String
a = 10
b = 20.5
print(str(a) + str(b)) # Conversão de Inteiro e Float para String

# Inteiro para Float
a = 10
print(float(a)) # Conversão de Inteiro para Float

# Float para Inteiro
b = 20.5
print(int(b)) # Conversão de Float para Inteiro

# Utilização do len() para obter o tamanho de uma string
a = "Python"
print(len(a)) # Tamanho da string "Python" é 6