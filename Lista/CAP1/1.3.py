# Faça um programa que leia dois números do tipo float e exiba a soma deles.
def main():
    x = float(input("")) # Float é usado (Ponto Flutuante) para soma de dois valores nesse caso
    y = float(input("")) 
    print(x+y) #
    
if __name__ == "__main__":
    main()